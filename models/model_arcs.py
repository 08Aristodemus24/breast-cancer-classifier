    model = Sequential([
        Dense(units=10, activation='relu',),
        Dense(units=10, activation='relu',),
        Dense(units=10, activation='relu',),
        Dense(units=10, activation='relu',),
        Dense(units=1, activation='linear'),
    ])

    model.compile(
        loss=bce_loss(from_logits=True),
        optimizer=Adam(learning_rate=0.001),
        metrics=[bce_metric(from_logits=True), BinaryAccuracy(threshold=0.5)]
    )

    return model

